import Foundation

struct Earthquakes: Codable {
    let type: String
    let metadata: Metadata
    let features: [Feature]
    let bbox: [Double]
}

struct Feature: Codable {
    let type: String
    let properties: Properties
    let geometry: Geometry
    let id: String
}

struct Geometry: Codable {
    let type: String
    let coordinates: [Double]
}

struct Properties: Codable {
    let mag: Double
    let place: String
    let time, updated, tz: Int
    let url, detail: String
    let felt, cdi, mmi, alert: JSONNull?
    let status: String
    let tsunami, sig: Int
    let net, code, ids, sources: String
    let types: String
    let nst: Int?
    let dmin, rms, gap: Double
    let magType, type, title: String
}

struct Metadata: Codable {
    let generated: Int
    let url, title: String
    let status: Int
    let api: String
    let count: Int
}

// MARK: Convenience initializers

extension Earthquakes {
    init(data: Data) throws {
        self = try JSONDecoder().decode(Earthquakes.self, from: data)
    }

    init?(_ json: String, using encoding: String.Encoding = .utf8) throws {
        guard let data = json.data(using: encoding) else { return nil }
        try self.init(data: data)
    }

    init?(fromURL url: String) throws {
        guard let url = URL(string: url) else { return nil }
        let data = try Data(contentsOf: url)
        try self.init(data: data)
    }

    func jsonData() throws -> Data {
        return try JSONEncoder().encode(self)
    }

    func jsonString() throws -> String? {
        return String(data: try self.jsonData(), encoding: .utf8)
    }
}

